class ProductModel {
  final String name;
  final String description;
  final String category;
  final String ownerName;
  final String contact;
  final double price;
  final String image;

  ProductModel({
    required this.name,
    required this.description,
    required this.category,
    required this.ownerName,
    required this.contact,
    required this.price,
    required this.image,
  });
}
